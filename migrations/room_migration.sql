USE stayunikl_db;

SET FOREIGN_KEY_CHECKS = 0;

-- 1. Create Rooms Table
CREATE TABLE IF NOT EXISTS rooms (
    id VARCHAR(10) PRIMARY KEY,
    floor_id INT NOT NULL,
    gender ENUM('Male', 'Female') NOT NULL,
    room_type VARCHAR(50) NOT NULL,
    capacity INT NOT NULL,
    status ENUM('Active', 'Maintenance') DEFAULT 'Active'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 2. Create Beds Table
CREATE TABLE IF NOT EXISTS beds (
    id VARCHAR(20) PRIMARY KEY,
    room_id VARCHAR(10) NOT NULL,
    label VARCHAR(5) NOT NULL, -- A, B, C, D
    status ENUM('Available', 'Occupied', 'Maintenance') DEFAULT 'Available',
    FOREIGN KEY (room_id) REFERENCES rooms(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

SET FOREIGN_KEY_CHECKS = 1;

-- 3. Add bed_id to Applications
-- Ensure columns exist for manual selection
SET @dbname = DATABASE();
SET @tablename = "applications";
SET @columnname = "bed_id";
SET @preparedStatement = (SELECT IF(
  (SELECT COUNT(*) FROM INFORMATION_SCHEMA.COLUMNS
   WHERE TABLE_SCHEMA = @dbname
     AND TABLE_NAME = @tablename
     AND COLUMN_NAME = @columnname) > 0,
  "SELECT 1",
  CONCAT("ALTER TABLE ", @tablename, " ADD COLUMN ", @columnname, " VARCHAR(50) AFTER room_type;")
));
PREPARE stmt FROM @preparedStatement;
EXECUTE stmt;
DEALLOCATE PREPARE stmt;

